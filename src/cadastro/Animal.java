package cadastro;

public class Animal {
	char raça;
	char cor;
	double peso;
	char tamanho;
	int idade;
	
	// getters and setters
	public char getRaça() {
		return raça;
	}
	public void setRaça(char raça) {
		this.raça = raça;
	}
	public char getCor() {
		return cor;
	}
	public void setCor(char cor) {
		this.cor = cor;
	}
	public double getPeso() {
		return peso;
	}
	public void setPeso(double peso) {
		this.peso = peso;
	}
	public char getTamanho() {
		return tamanho;
	}
	public void setTamanho(char tamanho) {
		this.tamanho = tamanho;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	
}
